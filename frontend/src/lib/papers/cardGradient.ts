function cyrb53Hash(str: string): number {
  let h1 = 0xdeadbeef;
  let h2 = 0x41c6ce57;
  for (let i = 0; i < str.length; i++) {
    const ch = str.charCodeAt(i);
    h1 = Math.imul(h1 ^ ch, 2654435761);
    h2 = Math.imul(h2 ^ ch, 1597334677);
  }
  h1 = Math.imul(h1 ^ (h1 >>> 16), 2246822507);
  h1 ^= Math.imul(h2 ^ (h2 >>> 13), 3266489909);
  h2 = Math.imul(h2 ^ (h2 >>> 16), 2246822507);
  h2 ^= Math.imul(h1 ^ (h1 >>> 13), 3266489909);
  return h2 >>> 0;
}

export function getCategoryCoverImage(
  categories: string[],
  postId: string | number,
  title?: string,
): string {
  const primaryCategory =
    normalizeCategory(categories?.[0] ?? "") ?? DEFAULT_CATEGORY;
  const pool = CATEGORY_IMAGES[primaryCategory] ?? FALLBACK_IMAGES;

  const seed = `${primaryCategory}::${String(postId)}::${title ?? ""}`;
  const index = cyrb53Hash(seed) % pool.length;

  return pool[index]!;
}

/** Stable pseudo-random diagonal gradient per paper (varies by id, same on every load). */
export function randomCardGradientStyle(paperId: string): string {
  let hash = 0;
  for (let i = 0; i < paperId.length; i++) {
    hash = (hash * 31 + paperId.charCodeAt(i)) >>> 0;
  }

  const nextHue = () => {
    hash = (hash * 1664525 + 1013904223) >>> 0;
    return hash % 360;
  };

  const h1 = nextHue();
  const h2 = nextHue();
  const h3 = nextHue();

  return `linear-gradient(135deg, hsl(${h1} 72% 74%) 0%, hsl(${h2} 68% 68%) 50%, hsl(${h3} 75% 76%) 100%)`;
}
